package Basic;

class TypeCasting
{
    public static void main(String[] args)
    {
//        Implicit Typecasting
        int x =50;
        double y =x;
        System.out.println(y);

//        Explict TypeCasting
        long a =500;
        int b = (int)a;
        System.out.println(b);
    }
}
